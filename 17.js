// Two angles of a triangle measure 15° and 85 °.
// What is the measure for the third angle?

// Given angles
const angle1 = 15;
const angle2 = 85;

// Calculate the third angle
const thirdAngle = 180 - angle1 - angle2;

// Print the result
console.log("The measure of the third angle is:", thirdAngle, "degrees.");
