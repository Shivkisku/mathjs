// Calculate the height of a cuboid
// which has a base area of 180 cm2 and volume is 900 cm3.

// Given values
const baseArea = 180; // in square centimeters
const volume = 900;   // in cubic centimeters

// Calculate the height
const height = volume / baseArea;

// Output the result
console.log(`Height of the cuboid: ${height} cm`);
