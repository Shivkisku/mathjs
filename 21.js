// Ariel was playing basketball. 1 of her shots went in the hoop.
// 2 of her shots did not go in the hoop. How many shots were there in total?


// Given information
const shotsInHoop = 1;
const shotsNotInHoop = 2;

// Calculate the total number of shots
const totalShots = shotsInHoop + shotsNotInHoop;

// Print the result
console.log(`Ariel took a total of ${totalShots} shots.`);
