// Melissa earns $5 an hour for babysitting. 
// If she babysat for 7 hours on Saturday and 6 hours on Sunday,
// how much money did she make in total?

// Given hourly rate and hours worked
const hourlyRate = 5;
const hoursSaturday = 7;
const hoursSunday = 6;

// Calculate total earnings
const totalEarnings = (hoursSaturday + hoursSunday) * hourlyRate;

// Print the result
console.log("Melissa made a total of $", totalEarnings);
